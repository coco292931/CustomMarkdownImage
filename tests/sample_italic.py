import pillowmd

"""
人工核对样例：单星号斜体 *italic* 及其与无序列表的消歧。

修复前：pillowmd 仅支持 **加粗**，不支持 *斜体*，单星号会原样显示成字面
"*文字*"，导致 LLM 输出大量散落星号、观感很差。

修复后：行内成对单星号识别为斜体，用 x 方向错切（shear）做伪斜体，并在
片段前后补空格留白；行首 "* "（星号+空格）仍按无序列表渲染。

运行后人工核对弹出图：
  - 斜体片段明显倾斜，前后星号被吃掉、无字面残留
  - 列表项符号正常，且列表项内的 *斜体* 也能倾斜
  - 不配对的单星号保持字面 *
"""


def run():
    text = (
        "普通 *斜体强调* 普通，normal *italic words* end\n\n"
        "**加粗** 和 *斜体* 与 ~~删除~~ 和 `代码` 同行\n\n"
        "* 列表项一\n"
        "* 列表项里有 *斜体* 词\n\n"
        "不配对的 * 单星号 保持字面"
    )
    img = pillowmd.SampleStyles.STYLE1.Render(text, title="斜体样例")
    img.image.show()


if __name__ == "__main__":
    run()
